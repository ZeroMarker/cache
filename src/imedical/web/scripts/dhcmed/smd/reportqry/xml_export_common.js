var XMLTool = new Object();
XMLTool.CreateXML = function(rootNodeName)
{
	var objDoc = new ActiveXObject("MSXML2.DOMDocument");
	objDoc.loadXML('<?xml version="1.0" encoding="GBK"?><' + rootNodeName + '/>');
	return objDoc;
}

XMLTool.CreateNode = function(objDoc, nodeName, objParent, nameSpace)
{
	if (nameSpace == null) nameSpace = "";
	var objNode = objDoc.createNode(1, nodeName, nameSpace);
	if (objParent) objParent.appendChild(objNode);
	return objNode;
}


XMLTool.CreateTextNode = function(objDoc, nodeName, value, objParent, nameSpace)
{
	if (nameSpace == null) nameSpace = "";
	var objNode = objDoc.createNode(1, nodeName, nameSpace);
	var objTxtNode = objDoc.createNode(3, nodeName, nameSpace);
	objTxtNode.text = value;
	objNode.appendChild(objTxtNode);
	if (objParent) objParent.appendChild(objNode);
	return objNode;
}

